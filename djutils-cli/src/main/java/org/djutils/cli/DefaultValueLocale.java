@Retention(RetentionPolicy.RUNTIME)
@Target({ElementType.TYPE, ElementType.FIELD, ElementType.METHOD})
public @interface DefaultValueLocale
{
    /**
     * The locale to use.
     * @return the locale to use
     */
    String value() default "en";
}
